function solve(input) {
    let countryName = String(input);

    if (countryName === "England") {
        console.log("English");
    } else if (countryName === "USA") {
        console.log("English");
    } else if (countryName === "Spain") {
        console.log("Spanish");
    } else if (countryName === "Argentina") {
        console.log("Spanish");
    } else if (countryName === "Mexico") {
        console.log("Spanish");
    } else {
        console.log("unknown");
    }

}
solve("USA");